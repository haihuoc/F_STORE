    public void Showdata()
    {
        String sql = "select * from Manufacturers";
        ResultSet rs = desktopapp.DesktopApp.connection.ExcuteQueryGetTable(sql);
        
        DefaultTableModel tbModel = (DefaultTableModel) TableNCC.getModel();
        tbModel.setRowCount(0);
        
        Object obj[] = new Object[10];
        
        try {
            while (rs.next()) {
                obj[0] = rs.getString("ManufacturerID");
                obj[1] = rs.getString("Name");
                obj[2] = rs.getString("Address");
                obj[3] = rs.getString("Phone");
                
                tbModel.addRow(obj);
                
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(new JFrame(),"Lỗi truy xuất dữ liệu ");
        }
    }
    
