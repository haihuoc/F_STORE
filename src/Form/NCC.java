    private boolean CheckNCC(String MaNcc)
    {
        String sql = "SELECT COUNT(*) AS Count FROM Products WHERE ManufacturerID ="+MaNcc;
        int productcount = 0;
        ResultSet rs =desktopapp.DesktopApp.connection.ExcuteQueryGetTable(sql);
        try {
            if (rs.next())
            {
                productcount = rs.getInt("Count");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return productcount>0;
    }

